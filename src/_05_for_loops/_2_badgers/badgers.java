package _05_for_loops._2_badgers;

public class badgers {
	public static void main(String[] args) {

		for (int j = 0; j < 2; j++) {

			for (int i = 0; i < 12; i++) {
				System.out.println("badger");
			}
			for (int i = 0; i < 2; i++) {
				System.out.println("mushroom");
			}
		}
		for (int i = 0; i < 1; i++ ) {
			System.out.println("A Snake!!!");
		}

	}
}
